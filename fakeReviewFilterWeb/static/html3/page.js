function showCurrentPageReviews(data, pagination) {
    reviewTbody = $('#reviews');
    reviewTbody.empty();

    for(var i = 0; i < data.length; ++i) {
        review = data[i];
        var username = $('<strong></strong>').addClass('text-left').addClass('col-lg-4').text(review[0]);
        var voteInfo = $('<p></p>').addClass('text-right').text(review[1]);
        var content = $('<p></p>').text(review[2]);

        var td = $('<td></td>');
        td.append(username).append(voteInfo).append(content);
        var tr = $('<tr></tr>');
        tr.append(td);
        reviewTbody.append(tr);
    }
}

